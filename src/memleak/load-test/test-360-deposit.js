// Function to sample JSON data
function sampleJsonData(sampleRate, totalRecords) {
  const sampledData = [];
  for (let i = 0; i < totalRecords; i++) {
    if (Math.random() < sampleRate) {
      sampledData.push(generateRandomJsonData());
    }
  }
  return sampledData;
}

